def showShapesAreaFormula(shape):
    if shape == "circle":
        print(" πr²")
    elif shape == "triangle":
        print("(1/2) * Base * Height")
    elif shape == "Square":
        print("side × side")
    elif shape == "parallelogram":
        print("Base × Height")
    elif shape == "trapezoid":
        print("1/2 * (Base1 + Base2) * Height")
    
       